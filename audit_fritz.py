# 12. 08_Compliance_Check.xlsx
try:
    wb = openpyxl.load_workbook(f"{extract_path}/08_Compliance_Check.xlsx")
    ws = wb.active
    rows = list(ws.iter_rows(values_only=True))
    has_status = False
    for r in rows:
        if r[0] == "CSRD OBLIGATION STATUS" and r[1] == "Obligated":
            has_status = True
    check("08_Compliance_Check.xlsx", has_status, "Obligation status not 'Obligated' or missing")
except Exception as e:
    check("08_Compliance_Check.xlsx", False, str(e))

print("="*60)
print(f"{'FILE':<40} | {'STATUS':<10} | {'ERROR'}")
print("="*60)
for name, status, err in results:
    print(f"{name:<40} | {status:<10} | {err}")
print("="*60)

failures = [r for r in results if r[1] == "❌ FAIL"]
if failures:
    sys.exit(1)
else:
    print("ALL CHECKS PASSED!")
    sys.exit(0)
